-module(counter_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

-define(DEFAULT, 0).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    InitVal = application:get_env(counter, init_val, ?DEFAULT),
    %InitVal = 0,
    counter_sup:start_link(InitVal).

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

